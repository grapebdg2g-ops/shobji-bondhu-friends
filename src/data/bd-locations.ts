// ── Helpers ─────────────────────────────────────────────
export const getDistricts = (): string[] => Object.keys(BD_LOCATIONS);

export const getUpazilas = (district: string | null | undefined): string[] => {
  if (!district) return [];
  return BD_LOCATIONS[district] ?? [];
};

export const getUpazilaCoords = (
  district: string | null | undefined,
  upazila: string | null | undefined,
): [number, number] | null => {
  if (!district || !upazila) return null;
  const dmap = UPAZILA_COORDS[district];
  if (!dmap) return null;
  return dmap[upazila] ?? null;
};
